p3 = [0.4, 0.3, 0.2, 0.1]
H3 = 0
for pi in p3:
    H3 -= pi * math.log(pi, 2)
print(H3, 'Энтропия меньше максимального значения для четырёх сообщений, что указывает на наличие предсказуемости в источнике информации.')

